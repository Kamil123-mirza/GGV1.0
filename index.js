function upd() {
  let cdt = document.querySelector("#ctd").value;
  let ou1 = document.getElementById("rp1");
  let ou2 = document.getElementById("rp2");
  let ou3 = document.getElementById("rp3");
  let ou4 = document.getElementById("rp4");
  let len = cdt.length;
  ou1.innerHTML = len;
  ou2.innerHTML = cdt.split(" ").length;
  ou3.innerHTML = cdt.split(" ").length * 0.7 + " s";
  if (cdt === "") {
    ou4.innerHTML = "note null";  // empty case
  }
  else if (cdt === cdt.toUpperCase()) {
    ou4.innerHTML = "UPPERCASE";  // all letters uppercase
  }
  else if (cdt === cdt.toLowerCase()) {
    ou4.innerHTML = "lowercase";  // all letters lowercase
  }
  else {
    ou4.innerHTML = "Mixed Case"; // अगर दोनों mix हैं
  }
}
